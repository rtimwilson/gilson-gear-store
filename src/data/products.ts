// Get product by slug
export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug)
}

// Get product by ID
export function getProductById(id: string): Product | undefined {
  return products.find(p => p.id === id)
}

// Get products by category
export function getProductsByCategory(category: string): Product[] {
  return products.filter(p => p.category === category)
}

// Get featured products (bestsellers + new)
export function getFeaturedProducts(limit = 8): Product[] {
  return products
    .filter(p => p.isBestSeller || p.isNew)
    .slice(0, limit)
}

// Get premium products
export function getPremiumProducts(limit = 6): Product[] {
  return products
    .filter(p => p.isPremium)
    .slice(0, limit)
}

// Get new arrivals
export function getNewArrivals(limit = 4): Product[] {
  return products
    .filter(p => p.isNew)
    .slice(0, limit)
}

// Get bestsellers
export function getBestsellers(limit = 6): Product[] {
  return products
    .filter(p => p.isBestSeller)
    .sort((a, b) => (b.soldCount || 0) - (a.soldCount || 0))
    .slice(0, limit)
}

// Get related products (same category, excluding current)
export function getRelatedProducts(product: Product, limit = 4): Product[] {
  return products
    .filter(p => p.category === product.category && p.id !== product.id)
    .slice(0, limit)
}

// Get bundle by slug
export function getBundleBySlug(slug: string): Bundle | undefined {
  return bundles.find(b => b.slug === slug)
}

// Get bundle products
export function getBundleProducts(bundle: Bundle): Product[] {
  return bundle.products
    .map(id => getProductById(id))
    .filter((p): p is Product => p !== undefined)
}
